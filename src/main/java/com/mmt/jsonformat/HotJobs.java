package com.mmt.jsonformat;

public class HotJobs
{
	private long jobId;
	private String jobName;
	private String education;
	private String experence;
	private String startSalary;
	private String endSalary;
	private String city;
	private String description;

	
	public long getJobId() {
		return jobId;
	}
	public void setJobId(long jobId) {
		this.jobId = jobId;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getEducation() {
		return education;
	}
	public void setEducation(String education) {
		this.education = education;
	}
	public String getExperence() {
		return experence;
	}
	public void setExperence(String experence) {
		this.experence = experence;
	}
	public String getStartSalary() {
		return startSalary;
	}
	public void setStartSalary(String startSalary) {
		this.startSalary = startSalary;
	}
	public String getEndSalary() {
		return endSalary;
	}
	public void setEndSalary(String endSalary) {
		this.endSalary = endSalary;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

	
}
